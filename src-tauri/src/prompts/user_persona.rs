//! 用户画像生成提示词模板
//! 
//! VD-023: 创建用户画像提示词模板

/// 用户画像生成提示词模板
/// 
/// 该模板用于指导 AI 生成结构化、详细的用户画像
pub const USER_PERSONA_TEMPLATE: &str = r#"你是一位专业的用户研究员，擅长通过产品定位和目标市场分析，创建详细、真实的用户画像。

## 任务
请根据以下产品信息，生成 3-5 个典型的用户画像（User Personas）。

## 产品信息
{product_info}

## 用户画像结构要求
请为每个用户画像包含以下信息：

### 基本信息
- **姓名**：一个真实感的中文或英文名
- **年龄**：具体年龄或年龄段（如 28 岁，或 25-35 岁）
- **职业**：具体的职位和行业
- **城市**：一线/新一线/二线城市等
- **收入水平**：月收入范围或消费能力描述

### 个人背景
- **教育背景**：学历和专业
- **工作经历**：相关的工作经验描述
- **技术能力**：对互联网产品的熟悉程度
- **生活方式**：日常生活状态和习惯

### 与产品相关的特征
- **核心需求**：该产品要解决的主要痛点（2-3 个）
- **使用场景**：在什么情况下会使用这个产品（3-4 个典型场景）
- **期望功能**：最希望产品具备的功能（3-5 个）
- **付费意愿**：对这类产品的付费态度和预算

### 行为特征
- **获取信息渠道**：通常通过哪些渠道发现新产品
- **决策因素**：选择产品时最看重的因素（如价格、口碑、功能等）
- **使用频率预期**：预计使用该产品的频率（每日/每周/每月）
- **推荐意愿**：是否愿意向他人推荐及原因

### 引言
一句能代表该用户典型想法的原话，用引号标注，体现其核心诉求。

## 输出格式要求

请按以下 Markdown 格式输出：

```markdown
# 用户画像

## 用户画像 1：[姓名] - [标签/关键词]

### 📋 基本信息
| 维度 | 描述 |
|------|------|
| 年龄 | [具体年龄] |
| 职业 | [职业描述] |
| 城市 | [城市级别] |
| 收入 | [收入水平] |

### 🎯 个人背景
- **教育背景**：[学历和专业]
- **工作经历**：[相关工作经历]
- **技术能力**：[技术水平描述]
- **生活方式**：[生活状态描述]

### 💡 与产品相关的特征
- **核心需求**：
  1. [需求 1]
  2. [需求 2]
  3. [需求 3]
  
- **使用场景**：
  1. [场景 1]
  2. [场景 2]
  3. [场景 3]
  
- **期望功能**：
  1. [功能 1]
  2. [功能 2]
  3. [功能 3]
  
- **付费意愿**：[付费态度描述]

### 🔍 行为特征
- **获取信息渠道**：[渠道列表]
- **决策因素**：[关键决策因素]
- **使用频率预期**：[预期频率]
- **推荐意愿**：[推荐态度]

### 💬 用户引言
"[一句代表性的用户原话]"

---

## 用户画像 2：[姓名] - [标签/关键词]
...

## 总结
简要总结这些用户画像的共同特征和差异点，以及产品设计时的优先级建议。
```

## 注意事项
1. **真实性**：用户画像要真实可信，避免虚构过于理想化的形象
2. **多样性**：3-5 个用户画像应覆盖不同的用户类型和使用场景
3. **具体性**：提供具体的细节和数据，避免模糊描述
4. **可操作性**：生成的用户画像要能直接指导产品设计和功能优先级
5. **篇幅**：每个用户画像控制在 300-500 字
6. **语言**：使用简洁、专业的中文表达

## 示例参考
如果产品信息是"一个帮助独立开发者管理项目进度的工具"，你应该生成类似这样的用户画像：
- 张三 - 忙碌的初创公司创始人
- 李四 - 追求效率的自由职业者
- 王五 - 注重数据的产品经理
等

现在，请根据上述产品信息生成 3-5 个详细的用户画像。"#;

/// 用户画像生成提示词
/// 
/// # Arguments
/// * `product_info` - 产品信息，包括产品定位、目标用户、核心功能等
/// 
/// # Returns
/// 返回完整的提示词字符串
pub fn generate_user_persona_prompt(product_info: &str) -> String {
    USER_PERSONA_TEMPLATE.replace("{product_info}", product_info)
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_generate_user_persona_prompt() {
        let product_info = "一个帮助独立开发者管理项目进度的 SaaS 工具";
        let prompt = generate_user_persona_prompt(product_info);
        
        assert!(prompt.contains("一个帮助独立开发者管理项目进度的 SaaS 工具"));
        assert!(prompt.contains("用户画像"));
        assert!(prompt.contains("基本信息"));
        assert!(prompt.contains("核心需求"));
    }

    #[test]
    fn test_template_structure() {
        // 验证模板包含所有必要的章节
        assert!(USER_PERSONA_TEMPLATE.contains("基本信息"));
        assert!(USER_PERSONA_TEMPLATE.contains("个人背景"));
        assert!(USER_PERSONA_TEMPLATE.contains("与产品相关的特征"));
        assert!(USER_PERSONA_TEMPLATE.contains("行为特征"));
        assert!(USER_PERSONA_TEMPLATE.contains("用户引言"));
        assert!(USER_PERSONA_TEMPLATE.contains("输出格式要求"));
    }
}
