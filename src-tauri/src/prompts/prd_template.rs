//! 
//! VD-017: 创建 PRD 生成提示词模板

/// PRD 生成提示词模板
/// 
/// 该模板用于指导 AI 生成结构化、专业的产品需求文档
pub const PRD_GENERATION_TEMPLATE: &str = r#"你是一位经验丰富的产品经理，擅长将简单的产品想法转化为完整、专业的产品需求文档（PRD）。

## 任务
请根据用户提供的产品想法，生成一份详细的产品需求文档。

## 用户想法
{idea}

## PRD 结构要求
请按照以下结构生成文档：

# 产品需求文档 - {product_name}

## 1. 产品概述
### 1.1 产品定位
用一句话清晰描述产品的核心价值和市场定位。

### 1.2 目标用户
明确描述产品的目标用户群体，包括主要用户和次要用户。

### 1.3 核心痛点
列出产品要解决的用户痛点（2-3 个）。

### 1.4 解决方案
简述产品如何解决上述痛点。

## 2. 市场分析
### 2.1 市场规模
估算目标市场的规模和增长潜力。

### 2.2 市场趋势
分析当前市场的主要发展趋势。

### 2.3 机会点
识别市场中的机会和空白点。

## 3. 竞品分析
### 3.1 主要竞品
列出 3-5 个主要竞争对手及其核心功能。

### 3.2 竞品优劣势
分析每个竞品优势和不足。

### 3.3 差异化策略
说明本产品与竞品的差异化定位。

## 4. 产品功能
### 4.1 功能架构
描述产品的整体功能架构（可用文字描述层级关系）。

### 4.2 核心功能详情
对每个核心功能进行详细描述：
- 功能名称
- 功能描述
- 使用场景
- 优先级（P0/P1/P2）

### 4.3 功能优先级排序
使用 MoSCoW 法则（Must have, Should have, Could have, Won't have）对功能进行排序。

## 5. 技术架构
### 5.1 技术栈选择
推荐适合的技术栈（前端、后端、数据库等）。

### 5.2 系统架构
描述系统的整体架构设计。

### 5.3 关键技术挑战
识别可能的技术难点和风险点。

## 6. 商业模式
### 6.1 收入模式
描述产品如何盈利（订阅、广告、交易抽成等）。

### 6.2 定价策略
给出初步的定价建议。

### 6.3 成本结构
分析主要的成本构成。

## 7. 运营策略
### 7.1 获客渠道
列出主要的用户获取渠道。

### 7.2 增长策略
描述用户增长和留存策略。

### 7.3 关键指标
定义衡量产品成功的关键指标（KPIs）。

## 8. 开发计划
### 8.1 MVP 范围
定义 MVP（最小可行产品）的功能范围。

### 8.2 开发阶段
将开发过程分为 2-3 个阶段，并说明每个阶段的目标。

### 8.3 时间估算
给出每个阶段的粗略时间估算（周为单位）。

## 9. 风险评估
### 9.1 市场风险
分析市场方面的风险和不确定性。

### 9.2 技术风险
识别技术实现上的风险。

### 9.3 运营风险
分析运营过程中可能遇到的风险。

### 9.4 应对策略
针对每个风险提出应对措施。

## 10. 总结
用 2-3 句话总结产品的核心价值和成功要素。

---

## 输出要求
1. **专业性**：使用专业的产品术语，但保持清晰易懂
2. **结构化**：严格按照上述章节结构组织内容
3. **具体性**：避免空泛描述，提供具体的功能点和数据
4. **可行性**：确保建议的方案在技术和商业上可行
5. **篇幅**：整体控制在 2000-3000 字
6. **格式**：使用 Markdown 格式，合理使用标题、列表、表格等

## 注意事项
- 如果用户的想法过于简单，请合理补充细节
- 对于不确定的信息，可以给出多个选项供用户选择
- 重点突出产品的差异化和创新点
- 保持客观理性的态度，避免过度承诺"#;

/// 生成 PRD 提示词
/// 
/// # Arguments
/// * `idea` - 用户的产品想法
/// * `product_name` - 产品名称（可选，如果为空则使用"未命名产品"）
/// 
/// # Returns
/// 返回完整的提示词字符串
pub fn generate_prd_prompt(idea: &str, product_name: Option<&str>) -> String {
    let name = product_name.unwrap_or("未命名产品");
    
    PRD_GENERATION_TEMPLATE
        .replace("{idea}", idea)
        .replace("{product_name}", name)
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_generate_prd_prompt() {
        let idea = "我想做一个帮助独立开发者管理项目进度的工具";
        let prompt = generate_prd_prompt(idea, Some("DevProgress"));
        
        assert!(prompt.contains("DevProgress"));
        assert!(prompt.contains(idea));
        assert!(prompt.contains("产品需求文档"));
    }

    #[test]
    fn test_generate_prd_prompt_default_name() {
        let idea = "一个在线购物网站";
        let prompt = generate_prd_prompt(idea, None);
        
        assert!(prompt.contains("未命名产品"));
        assert!(prompt.contains(idea));
    }
}
