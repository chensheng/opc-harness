/**
 * 像素家具组件
 */
export function PixelFurniture({ type, size = 64, className = '' }: PixelFurnitureProps) {
  const canvasRef = React.useRef<HTMLCanvasElement>(null)

  React.useEffect(() => {
    const canvas = canvasRef.current
    if (!canvas) return

    const ctx = canvas.getContext('2d')
    if (!ctx) return

    const pattern = FURNITARY_PATTERNS[type]
    if (!pattern) return

    const pixelSize = size / 16

    // 清空画布
    ctx.clearRect(0, 0, size, size)

    // 绘制像素
    pattern.forEach((row, y) => {
      row.forEach((colorKey, x) => {
        if (colorKey && FURNITARY_COLORS[colorKey]) {
          ctx.fillStyle = FURNITARY_COLORS[colorKey]
          ctx.fillRect(x * pixelSize, y * pixelSize, pixelSize, pixelSize)
        }
      })
    })
  }, [type, size])

  return (
    <canvas
      ref={canvasRef}
      width={size}
      height={size}
      className={className}
      style={{ imageRendering: 'pixelated' }}
    />
  )
}

